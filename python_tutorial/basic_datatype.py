a = 10              # integer(int)
print(a)

b = 2.15             # floating point(float)
print(b)

c = 1 + 3.1j        # complex number(complex)
print(c)

d = "Hello World"   # string(str)
print(d)

e = [1,2,"3",4,5.0] # list(list)
print(e)

f = (1,2,"3",4,5.0) # tuple(tuple)
print(f)

g = {'a':5, 2:'b'}  # dictionary(dict)
print(g)

h = True            # boolean(bool)
print(h)
